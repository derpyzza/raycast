void DrawMap(int width, int height, int size)
{
    for (int y = 0; y < height; y++)
    {
        for (int x = 0; x < width; x++)
        {
            if (map[y][x] == 1)
            {
                DrawRectangle(x * size, y * size, size - cc, size - cc, WHITE);
            }
            else if (map[y][x] == 0)
            {
                DrawRectangle(x * size, y * size, size - cc, size - cc, BLACK);
            }
        }
    }
}

